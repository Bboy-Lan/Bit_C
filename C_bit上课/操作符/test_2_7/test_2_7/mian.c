#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>

int main()
{
	//整数在内存中存储的是二进制补码
	//左移操作符 <<   移动看符号 多余部分舍去
	//右移操作符 >>
	int a = 5;
	int b1 = a << 1;
	int b2 = a >> 1;
	//原码
	//0 0000000 00000000 00000000 00000101 
	//反码
	//0 0000000 00000000 00000000 00000101
	//补码
	//0 0000000 00000000 00000000 00000101
	printf("%d\n", b1);
	//0 0000000 00000000 00000000 00001010    ----  10
	printf("%d\n", b2);
	//0 0000000 00000000 00000000 00000010    ----  2

	int c = -1;
	int d1 = c << 1;
	int d2 = c >> 1;
	//原码
	//1 0000000 00000000 00000000 00000001 
	//反码
	//1 1111111 11111111 11111111 11111110
	//补码
	//1 1111111 11111111 11111111 11111111
	printf("%d\n", d1);
	//1 1111111 11111111 11111111 11111110
	//转换成原码后结果是 
	//1 0000000 00000000 00000000 00000010   -----  -2
	printf("%d\n", d2);
	//1 1111111 11111111 11111111 11111111
	//转换后结果
	//1 0000000 00000000 00000000 00000001  ------  -1
	return 0;
}